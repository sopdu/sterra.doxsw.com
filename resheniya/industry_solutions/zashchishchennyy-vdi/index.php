 <strong>Для защиты серверной части</strong> можно использовать как шлюз безопасности в виде виртуальной машины для популярных гипервизоров (VMware ESX, Citrix XenServer, Parallels, KVM) – С-Терра Виртуальный Шлюз, так и традиционный С-Терра Шлюз на аппаратной платформе
