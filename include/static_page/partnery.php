</p>
<h2>Производители дополнительных средств аутентификации</h2>
<p>
	 Продукты CSP VPN (прежде всего – CSP VPN Client) работают со средствами дополнительной аутентификации пользователей, в частности с носителями криптографических ключей (смарт-карты, USB-токены) и персональные устройства для генерации одноразовых паролей.
</p>
<p>
	 Данные средства персональной аутентификации обладают следующими <b>преимуществами</b>:
</p>
<ul>
	<li><b>усиливают</b> защищенность системы в целом, предоставляя дополнительную (двухфакторную) аутентификацию;</li>
	<li>существенно <b>упрощают</b> как администрирование безопасности в рамках системы, так и работу индивидуального пользователя;</li>
	<li>применяются <b>для множества систем безопасности</b> – от сетевого уровня (VPN) до доступа к операционным системам, базам данных и приложениям.</li>
</ul>
<p>
	 Нашими технологическими партнерами в построении систем аутентификации пользователей являются компании «Аладдин» и «Демос».
</p>