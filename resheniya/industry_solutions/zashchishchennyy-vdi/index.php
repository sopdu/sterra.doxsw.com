	<p>
 <strong>На клиентской стороне</strong> для доступа к инфраструктуре виртуальных рабочих столов <strong>используется защищенный терминал</strong>, состоящий из тонкого клиента и специального загрузочного носителя (СЗН) «Пост».
	</p>
	<p>
 <strong>При этом обеспечивается</strong>:
	</p>
	<ul>
		<li>Возможность перехода к полностью виртуальной инфраструктуре сети компании.</li>
		<li>Полная функциональность аппаратного VPN-шлюза С-Терра Шлюз.</li>
		<li>Доверенная загрузка эталонной среды. При каждом запуске защищенного терминала с СЗН загружается эталонная программная среда.</li>
		<li>Замкнутая программная среда. Ни пользователь, ни злоумышленник не имеет возможности добавить какие-либо компоненты в программную среду терминала.</li>
		<li>Строгая двухфакторная аутентификация для построения защищенного соединения и доступа к виртуальным рабочим столам. В решении возможно использование различных токенов, например, компании «Aladdin» (eToken, eToken PRO, NG-FLASH) и «Актив» (Рутокен S, Рутокен ЭЦП).</li>
		<li>Изолированное сетевое соединение с инфраструктурой VDI. Целевой трафик передается по защищенному VPN-туннелю, при этом обеспечивается конфиденциальность и целостность передаваемой информации. Остальной трафик либо запрещен, либо передается через корпоративный proxy-сервер, в зависимости от политики безопасности компании заказчика.</li>
		<li>Подпись документов локальной электронной подписью (ЭП). Использование ЭП в инфраструктуре виртуальных рабочих столов достигается за счет прозрачного пробрасывания токена в терминальную сессию.</li>
		<li>Поддержка периферийных устройств, таких как принтеры и сканеры.</li>
		<li>Адаптация решения под требования заказчика.</li>
		<li>Большой выбор функционального программного обеспечения для работы удаленного пользователя с различными сервисами компании.</li>
	</ul>
	<p>
		 Использование замкнутой программной среды и СЗН минимизирует воздействие агрессивной информационной среды на работу с важной информацией, а также снижает риски, связанные с возможными деструктивными действиями пользователей. Кроме того, заказчик может отказаться от применения антивирусного программного обеспечения на рабочих местах пользователей. Это позволяет не только сэкономить средства, но и существенно облегчить процесс эксплуатации терминалов, поскольку нет необходимости контролировать их конфигурацию и обновлять антивирусные базы данных.
