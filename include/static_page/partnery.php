	 Компания «С-Терра СиЭсПи» является российским производителем средств сетевой защиты.
</p>
<p>
	 Мы предлагаем российские решения сетевой защиты информации дополняющие решения Cisco Systems. Техническая стратегия и бизнес-модель «С-Терра СиЭсПи» получили одобрение Представительства Cisco Systems в России и нашей компании присвоен статус технологического партнера Cisco Systems (Cisco Solution Technology Integrator).
</p>
<p>
	 Наша бизнес-модель основывается на схеме непрямых продаж, мы предлагаем нашу продукцию <i>системным интеграторам</i>, в первую очередь – партнерам Cisco Systems в России и странах СНГ.
</p>
<p>
	 При производстве мы сотрудничаем с рядом <i>технологических партнеров</i>.
</p>
<h2>Производители сертифицированных криптографических средств защиты</h2>
<p>
	 Одним из важных преимуществ продуктов CSP VPN является открытость криптографической архитектуры – в продуктах CSP VPN может использоваться криптографическая библиотека любого независимого производителя. Мы работаем с авторитетными российскими производителями криптографических средств защиты – компаниями «Крипто-Про», «Сигнал-КОМ», «ЛИССИ».
</p>
